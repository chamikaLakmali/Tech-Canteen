    private void registerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_registerMouseClicked
        
        char [] pass=password.getPassword();
        String pwrd=("" + new String(pass));
        String mail=email.getText();
        

        int status=1;
        int mailStat=1;
        if (id.getText().equals("") || name.getText().equals("") || recidence.getText().equals("") || phone.getText().equals("  format : - XXX-XXXXXXX")|| phone.getText().equals("") ||  email.getText().equals("")|| password.getPassword().toString().equals("") || crmpassword.getPassword().toString().equals("")){
            error.setText("Please fill all the fields..");
        }
        
        else{
            // validating passwords
            if (Arrays.equals(password.getPassword(), crmpassword.getPassword())) {
                
                // validating email and phone number 
                String phonePatter="\\d{3}-\\d{7}"; // pattern for phone number
                String emailPattern="[a-zA-Z].[\\w\\W]*\\.[a-zA-Z]{1,3}"; //pattern for email
                
                
                if (Pattern.compile(phonePatter).matcher(phone.getText()).matches()) { //validating phone number
                    
                    if (Pattern.compile(emailPattern).matcher(email.getText()).matches()) { // validating email address
                        
                        Session session1=HybernetSessionFctoryUser.getSessionFactory().openSession();
                        Transaction t2=session1.beginTransaction();

                        String sql="FROM Studentnstaff";
                        Query query=session1.createQuery(sql);
//
                        java.util.List <Studentnstaff>userList=query.list();
                        for(Iterator<Studentnstaff> userIterator=userList.iterator();userIterator.hasNext();){
                            Studentnstaff studentStaff=userIterator.next();
                            if (studentStaff.getRegNo().equals(id.getText())){
                                status=0;
                            }
                            if(studentStaff.getEmail().equals(email.getText())){
                                mailStat=0;
                            }
                           
                        }
                        if(status==1){
                            if (mailStat==1){      
                                 
                                // generating random string
                                int leftLimit = 97; // letter 'a'
                                int rightLimit = 122; // letter 'z'
                                int targetStringLength = 5;
                                Random random = new Random();

                                String generatedString = random.ints(leftLimit, rightLimit + 1)
                                  .limit(targetStringLength)
                                  .collect(StringBuilder::new, StringBuilder::appendCodePoint, StringBuilder::append)
                                  .toString();
                                Email.SendEmails.VerifyIdentity(generatedString,mail);
                                error.setText("Verification code will be sent to your email.....");
                                JFrame frame = new JFrame();
                                String rveri = JOptionPane.showInputDialog(frame, "We have send you a verification code to your email. Please enter the verification code :");

                                if (rveri!=null){
                                    error.setText("");
                                    if (rveri.equals(generatedString)){
                                        Studentnstaff user=new Studentnstaff();
                                        user.setRegNo(id.getText());
                                        user.setName(name.getText());
                                        user.setResidence(recidence.getText());
                                        user.setTele(phone.getText());
                                        user.setEmail(email.getText());
                                        user.setPassword(pwrd);
                                        user.setPoints(50);
                                        session1.save(user);
                                        session1.getTransaction().commit();

                                        // sending email to the user
                                        SendEmails email=new SendEmails();
                                        email.sendEmailToSt(mail);

                                        Login login=new Login();
                                        login.setVisible(true);
                                        this.dispose();
                                    }
                                    else{
                                        error.setText("Invalid confirmation code");
                                    }

                                }
                                
                            }
                            else{
                                email.setForeground(Color.red);
                                error.setText("This email address has already been used in the system..");
                            }
                        }
                        
                        else if (status==0){
                            id.setForeground(Color.red);
                            error.setText("Registration number already exists..");
                        }
                    }
                    
                    
                    else{
                        email.setForeground(Color.red);
                        error.setText("Please enter a valid email address...");
                    }
                    
                }
                else{
                    phone.setForeground(Color.red);
                    error.setText("Please enter the number in 'XXX-XXXXXXX' format");
                }
                
            }
            else{
                
                error.setText("Passwords are not maching..");
            }
            
           
        }
    }//GEN-LAST:event_registerMouseClicked

